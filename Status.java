/**
 * The Status enum represents the different statuses of a library item.
 * It can be one of the following:
 * - AVAILABLE: The item is available for borrowing.
 * - CHECKED_OUT: The item has been checked out by a borrower.
 * - OVERDUE: The item is overdue and has not been returned on time.
 */
public enum Status {
    AVAILABLE,
    CHECKED_OUT,
    OVERDUE
}


